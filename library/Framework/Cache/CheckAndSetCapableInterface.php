namespace Framework\Cache;

/**
 * A storage that is capable of performing a "check and set" operation.
 *
 */
interface CheckAndSetCapableInterface
{
    /**
     * Compare and swap an item.
     *
     * Performs a "check and set" operation, so that the item will be stored only if no other client has updated
     * it since it was last fetched by this client. The check is done via the cas token which is a unique value
     * assigned to the existing item.
     *
     * @param float $casToken unique value associated with an existing item.
     * @param string $key the key under which to store the item.
     * @param mixed $value The value to store.
     * @return bool true on success, false on failure.
     */
    public function cas($casToken, $key, $value);
}
